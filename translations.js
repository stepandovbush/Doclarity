// Translation Manager
class TranslationManager {
    constructor() {
        this.currentLanguage = localStorage.getItem('clearpath_language') || 'en';
        this.init();
    }

    init() {
        this.applyTranslations();
        this.setupLanguageListener();
    }

    setLanguage(lang) {
        if (translations[lang]) {
            this.currentLanguage = lang;
            localStorage.setItem('clearpath_language', lang);
            this.applyTranslations();
            this.updateChatStatus();
            if (window.chatbot) {
                window.chatbot.currentLanguage = lang;
            }
        }
    }

    applyTranslations() {
        const t = translations[this.currentLanguage];

        document.querySelectorAll('[data-i18n]').forEach(el => {
            const key = el.getAttribute('data-i18n');
            if (t[key]) {
                el.textContent = t[key];
            }
        });

        document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
            const key = el.getAttribute('data-i18n-placeholder');
            if (t[key]) {
                el.placeholder = t[key];
            }
        });

        const badge = document.getElementById('languageBadge');
        if (badge) {
            const langNames = {
                en: 'English',
                es: 'Español',
                ar: 'العربية',
                zh: '中文',
                fr: 'Français',
                de: 'Deutsch',
                it: 'Italiano',
                ja: '日本語',
                hi: 'हिन्दी',
            };
            badge.textContent = langNames[this.currentLanguage] || 'English';
        }

        document.documentElement.lang = this.currentLanguage;

        if (this.currentLanguage === 'ar') {
            document.documentElement.dir = 'rtl';
            document.body.style.direction = 'rtl';
        } else {
            document.documentElement.dir = 'ltr';
            document.body.style.direction = 'ltr';
        }
    }

    setupLanguageListener() {
        const languageSelect = document.getElementById('language');
        if (languageSelect) {
            languageSelect.addEventListener('change', (e) => {
                this.setLanguage(e.target.value);
            });
            languageSelect.value = this.currentLanguage;
        }
    }

    updateChatStatus() {
        const statusEl = document.getElementById('chatStatus');
        if (statusEl) {
            const langNames = {
                en: 'Ready to help',
                es: 'Listo para ayudar',
                ar: 'جاهز للمساعدة',
                zh: '准备帮助',
                fr: 'Prêt à aider',
                de: 'Bereit zu helfen',
                it: 'Pronto ad aiutare',
                ja: 'お手伝いの準備ができています',
                hi: 'मदद के लिए तैयार',
            };
            statusEl.textContent = langNames[this.currentLanguage] || 'Ready to help';
        }
    }

    t(key) {
        return translations[this.currentLanguage][key] || translations['en'][key] || key;
    }
}

if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', () => {
        window.i18n = new TranslationManager();
    });
} else {
    window.i18n = new TranslationManager();
}
